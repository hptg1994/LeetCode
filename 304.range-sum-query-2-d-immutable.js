/**
 * @param {number[][]} matrix
 */
var NumMatrix = function (matrix) {
  this.matrix = matrix;
};

/**
 * @param {number} row1
 * @param {number} col1
 * @param {number} row2
 * @param {number} col2
 * @return {number}
 */
NumMatrix.prototype.sumRegion = function (row1, col1, row2, col2) {
  let sum = 0;
  for (let rowStart = row1; rowStart <= row2; rowStart++) {
    for (let colStart = col1; colStart <= col2; colStart++) {
      sum += this.matrix[rowStart][colStart];
    }
  }
  return sum;
};

